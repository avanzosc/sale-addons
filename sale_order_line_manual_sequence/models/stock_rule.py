from odoo import models


class StockRule(models.Model):
    _inherit = "stock.rule"

    def _get_stock_move_values(
        self,
        product_id,
        product_qty,
        product_uom,
        location_id,
        name,
        origin,
        company_id,
        values,
    ):
        result = super()._get_stock_move_values(
            product_id,
            product_qty,
            product_uom,
            location_id,
            name,
            origin,
            company_id,
            values,
        )
        if "sale_line_id" in values and values.get("sale_line_id", False):
            line = self.env["sale.order.line"].browse(values.get("sale_line_id"))
            result["manual_sequence"] = line.manual_sequence
        return result
